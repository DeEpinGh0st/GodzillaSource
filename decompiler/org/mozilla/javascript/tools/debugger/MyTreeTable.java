class MyTreeTable
  extends JTreeTable
{
  private static final long serialVersionUID = 3457265548184453049L;
  
  public MyTreeTable(VariableModel model) {
    super(model);
  }



  
  public JTree resetTree(TreeTableModel treeTableModel) {
    this.tree = new JTreeTable.TreeTableCellRenderer(this, (TreeModel)treeTableModel);

    
    setModel((TableModel)new TreeTableModelAdapter(treeTableModel, (JTree)this.tree));

    
    JTreeTable.ListToTreeSelectionModelWrapper selectionWrapper = new JTreeTable.ListToTreeSelectionModelWrapper(this);
    
    this.tree.setSelectionModel((TreeSelectionModel)selectionWrapper);
    setSelectionModel(selectionWrapper.getListSelectionModel());

    
    if (this.tree.getRowHeight() < 1)
    {
      setRowHeight(18);
    }

    
    setDefaultRenderer(TreeTableModel.class, (TableCellRenderer)this.tree);
    setDefaultEditor(TreeTableModel.class, (TableCellEditor)new JTreeTable.TreeTableCellEditor(this));
    setShowGrid(true);
    setIntercellSpacing(new Dimension(1, 1));
    this.tree.setRootVisible(false);
    this.tree.setShowsRootHandles(true);
    DefaultTreeCellRenderer r = (DefaultTreeCellRenderer)this.tree.getCellRenderer();
    r.setOpenIcon(null);
    r.setClosedIcon(null);
    r.setLeafIcon(null);
    return (JTree)this.tree;
  }




  
  public boolean isCellEditable(EventObject e) {
    if (e instanceof MouseEvent) {
      MouseEvent me = (MouseEvent)e;





      
      if (me.getModifiers() == 0 || ((me.getModifiers() & 0x410) != 0 && (me.getModifiers() & 0x1ACF) == 0)) {












        
        int row = rowAtPoint(me.getPoint());
        for (int counter = getColumnCount() - 1; counter >= 0; 
          counter--) {
          if (TreeTableModel.class == getColumnClass(counter)) {
            MouseEvent newME = new MouseEvent((Component)this.tree, me.getID(), me.getWhen(), me.getModifiers(), me.getX() - (getCellRect(row, counter, true)).x, me.getY(), me.getClickCount(), me.isPopupTrigger());




            
            this.tree.dispatchEvent(newME);
            break;
          } 
        } 
      } 
      if (me.getClickCount() >= 3) {
        return true;
      }
      return false;
    } 
    if (e == null) {
      return true;
    }
    return false;
  }
}
